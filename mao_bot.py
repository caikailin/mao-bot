def get_next_segment(config):
    """获取当前应该推送的分段"""
    idx = config.get('current_index', 0)
    if idx >= len(SEGMENTS):
        return None
    return SEGMENTS[idx]


def build_card(segment):
    """构建飞书消息卡片"""
    total = len(SEGMENTS)
    now = datetime.now().strftime('%Y-%m-%d %H:%M')
    return {
        'msg_type': 'interactive',
        'card': {
            'header': {
                'title': {
                    'tag': 'plain_text',
                    'content': '\U0001f4d6 ' + segment['work'] + ' · ' + segment['title'],
                },
                'template': 'blue',
            },
            'elements': [
                {
                    'tag': 'div',
                    'text': {
                        'tag': 'lark_md',
                        'content': '**\U0001f4dc 原文摘录**\n>' + segment['original'],
                    },
                },
                {'tag': 'hr'},
                {
                    'tag': 'div',
                    'text': {
                        'tag': 'lark_md',
                        'content': '**\U0001f4a1 ' + segment['insight'] + '**',
                    },
                },
                {'tag': 'hr'},
                {
                    'tag': 'div',
                    'text': {
                        'tag': 'lark_md',
                        'content': '**\U0001f50d ' + segment['example'] + '**',
                    },
                },
                {
                    'tag': 'note',
                    'elements': [
                        {
                            'tag': 'plain_text',
                            'content': (
                                '毛泽东著作推送'
                                ' · 第 ' + str(segment['id']) + '/' + str(total) + ' 段'
                                ' · ' + now
                            ),
                        }
                    ],
                },
            ],
        },
    }


def send(segment, webhook_url):
    """发送消息到飞书机器人"""
    data = build_card(segment)
    payload = json.dumps(data, ensure_ascii=False).encode('utf-8')
    req = urllib.request.Request(
        webhook_url,
        data=payload,
        headers={'Content-Type': 'application/json; charset=utf-8'},
        method='POST',
    )
    try:
        with urllib.request.urlopen(req, timeout=30) as resp:
            result = resp.read().decode('utf-8')
            return json.loads(result)
    except urllib.error.HTTPError as e:
        return {'error': f'HTTP {e.code}: {e.read().decode("utf-8")}'}
    except urllib.error.URLError as e:
        return {'error': f'网络错误: {e.reason}'}
    except Exception as e:
        return {'error': str(e)}


def load_config(config_path):
    """加载配置文件"""
    if not os.path.exists(config_path):
        return {'webhook_url': '', 'current_index': 0}
    with open(config_path, 'r', encoding='utf-8') as f:
        return json.load(f)


def save_config(config_path, config):
    """保存配置文件"""
    with open(config_path, 'w', encoding='utf-8') as f:
        json.dump(config, f, ensure_ascii=False, indent=2)


def main():
    config_path = os.path.join(os.path.dirname(os.path.abspath(__file__)), 'config.json')
    config = load_config(config_path)

    # 优先使用环境变量 FEISHU_WEBHOOK（GitHub Actions 场景）
    webhook = os.environ.get('FEISHU_WEBHOOK') or config.get('webhook_url', '')
    if not webhook:
        print('ERROR: webhook_url not configured (set FEISHU_WEBHOOK env var or config.json)')
        sys.exit(1)

    idx = config.get('current_index', 0)
    total = len(SEGMENTS)

    if idx >= total:
        print(f'All {total} segments delivered! Reset current_index=0 to resend.')
        return

    segment = get_next_segment(config)
    if not segment:
        return

    print(f'Sending ({idx+1}/{total}): {segment["work"]} - {segment["title"]}')
    result = send(segment, webhook)
    if 'error' in result:
        print(f'FAILED: {result["error"]}')
        sys.exit(1)

    config['current_index'] = idx + 1
    save_config(config_path, config)
    remaining = total - (idx + 1)
    print(f'OK! Progress: {idx+1}/{total}, remaining: {remaining}')
    if remaining > 0:
        print(f'Next: {SEGMENTS[idx+1]["work"]} - {SEGMENTS[idx+1]["title"]}')
    else:
        print('All done!')


if __name__ == '__main__':
    main()
